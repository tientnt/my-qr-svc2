CREATE SCHEMA IF NOT EXISTS `qrsvc` DEFAULT CHARACTER SET utf8 ;

CREATE TABLE IF NOT EXISTS `qrsvc`.`route` (
  `id` BIGINT(20) NOT NULL AUTO_INCREMENT,
  `svc` VARCHAR(32) NOT NULL,
  `hash_value` BINARY(32) NOT NULL,
  `updated_at` DATETIME NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (`id`),
  UNIQUE INDEX `hash_UNIQUE` (`hash_value` ASC))
ENGINE = InnoDB
DEFAULT CHARACTER SET = utf8;